// ── Context ────────────────────────────────────────────────────────────────────

const dictionaries: Record<Locale, Dictionary> = { th, en, zh, ja };
const englishKeys = new Map<string, MessageKey>(
  (Object.keys(en) as MessageKey[]).map((key) => [en[key], key]),
);

export interface LocaleContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: MessageKey) => string;
  translateLiteral: (source: string) => string;
  authError: (message: string, fallback: MessageKey) => string;
}

const LocaleContext = createContext<LocaleContextValue | null>(null);

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    const stored = window.localStorage.getItem("petradar:locale") as Locale | null;
    const browser = navigator.language.toLowerCase();
    const detected: Locale = browser.startsWith("th") ? "th" : browser.startsWith("zh") ? "zh" : browser.startsWith("ja") ? "ja" : "en";
    setLocaleState(stored === "th" || stored === "en" || stored === "zh" || stored === "ja" ? stored : detected);
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale === "zh" ? "zh-CN" : locale;
  }, [locale]);

  const value = useMemo<LocaleContextValue>(() => ({
    locale,
    setLocale(next) {
      setLocaleState(next);
      window.localStorage.setItem("petradar:locale", next);
    },
    t: (key) => dictionaries[locale][key],
    translateLiteral(source) {
      const key = englishKeys.get(source);
      return key ? dictionaries[locale][key] : translateAppPhrase(locale, source);
    },
    authError(message, fallback) {
      const normalized = message.toLowerCase();
      if (normalized.includes("invalid email or password")) return dictionaries[locale]["error.invalidCredentials"];
      if (normalized.includes("email is already registered")) return dictionaries[locale]["error.emailRegistered"];
      if (normalized.includes("email must be an email")) return dictionaries[locale]["error.validEmail"];
      if (normalized.includes("password must be longer")) return dictionaries[locale]["error.passwordLength"];
      return message || dictionaries[locale][fallback];
    },
  }), [locale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useI18n() {
  const value = useContext(LocaleContext);
  if (!value) throw new Error("useI18n must be used within LocaleProvider");
  return value;
}

