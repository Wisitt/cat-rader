const catalog = new Map<string, Record<Exclude<Locale, "en">, string>>(
  phrases.map(([en, th, zh, ja]) => [en, { th, zh, ja }]),
);
const lowerCatalog = new Map<string, Record<Exclude<Locale, "en">, string>>(
  phrases.map(([en, th, zh, ja]) => [en.toLowerCase(), { th, zh, ja }]),
);

export function translateAppPhrase(locale: Locale, source: string) {
  if (locale === "en") return source;
  const direct = catalog.get(source)?.[locale] ?? lowerCatalog.get(source.toLowerCase())?.[locale];
  if (direct) return direct;

  const suffix = source.match(/([:*]+)$/)?.[0];
  if (suffix) {
    const core = source.slice(0, -suffix.length).trimEnd();
    const translated = catalog.get(core)?.[locale] ?? lowerCatalog.get(core.toLowerCase())?.[locale];
    if (translated) return `${translated}${suffix}`;
  }

  const count = source.match(/^(\d+)\s+(cases|records|reports|matches|pets)\s+(found|shown)?$/i);
  if (count) {
    const [, amount, noun] = count;
    if (locale === "th") return `${amount} ${noun.toLowerCase() === "pets" ? "ตัว" : "รายการ"}`;
    if (locale === "zh") return `${amount} ${noun.toLowerCase() === "pets" ? "只" : "条"}`;
    return `${amount}${noun.toLowerCase() === "pets" ? "匹" : "件"}`;
  }

  const distance = source.match(/^About\s+(\d+(?:\.\d+)?\s*(?:km|m|mi))$/i);
  if (distance) {
    if (locale === "th") return `ประมาณ ${distance[1]}`;
    if (locale === "zh") return `约 ${distance[1]}`;
    return `約${distance[1]}`;
  }

  const metricDistance = source.match(/^(\d+(?:\.\d+)?)\s+meters?$/i);
  if (metricDistance) {
    if (locale === "th") return `${metricDistance[1]} เมตร`;
    if (locale === "zh") return `${metricDistance[1]} 米`;
    return `${metricDistance[1]}メートル`;
  }

  const distanceAndTime = source.match(/^([\d.]+)\s+km away\s+·\s+(\d+)\s+hours?\s+ago$/i);
  if (distanceAndTime) {
    if (locale === "th") return `ห่าง ${distanceAndTime[1]} กม. · ${distanceAndTime[2]} ชม. ที่แล้ว`;
    if (locale === "zh") return `距 ${distanceAndTime[1]} 公里 · ${distanceAndTime[2]} 小时前`;
    return `${distanceAndTime[1]}km先 · ${distanceAndTime[2]}時間前`;
  }

  const communityRating = source.match(/^([\d.]+)\s+community rating$/i);
  if (communityRating) {
    if (locale === "th") return `คะแนนจากชุมชน ${communityRating[1]}`;
    if (locale === "zh") return `社区评分 ${communityRating[1]}`;
    return `コミュニティ評価 ${communityRating[1]}`;
  }

  const activeSince = source.match(/^(.+)\s+·\s+Active since\s+(\d{4})$/i);
  if (activeSince) {
    if (locale === "th") return `${activeSince[1]} · ใช้งานตั้งแต่ ${activeSince[2]}`;
    if (locale === "zh") return `${activeSince[1]} · 自 ${activeSince[2]} 年活跃`;
    return `${activeSince[1]} · ${activeSince[2]}年から活動`;
  }

  const lostPetName = source.match(/^Lost pet\s+·\s+(.+)$/i);
  if (lostPetName) {
    if (locale === "th") return `สัตว์เลี้ยงหาย · ${lostPetName[1]}`;
    if (locale === "zh") return `走失宠物 · ${lostPetName[1]}`;
    return `迷子ペット · ${lostPetName[1]}`;
  }

  const petSummary = source.match(/^(.+)\s+·\s+(Male|Female)\s+·\s+(\d+)\s+years?$/i);
  if (petSummary) {
    const [, species, sex, years] = petSummary;
    const translatedSpecies =
      catalog.get(species)?.[locale] ?? lowerCatalog.get(species.toLowerCase())?.[locale] ?? species;
    const translatedSex =
      catalog.get(sex)?.[locale] ?? lowerCatalog.get(sex.toLowerCase())?.[locale] ?? sex;
    if (locale === "th") return `${translatedSpecies} · ${translatedSex} · ${years} ปี`;
    if (locale === "zh") return `${translatedSpecies} · ${translatedSex} · ${years} 岁`;
    return `${translatedSpecies} · ${translatedSex} · ${years}歳`;
  }

  const lastSeen = source.match(/^Last seen\s+(in|near)\s+(.+)$/i);
  if (lastSeen) {
    const location = lastSeen[2];
    if (locale === "th") return `พบล่าสุดที่ ${location}`;
    if (locale === "zh") return `最后出现于 ${location}`;
    return `最終目撃：${location}`;
  }

  const seenNear = source.match(/^Seen near\s+(.+)$/i);
  if (seenNear) {
    if (locale === "th") return `พบใกล้ ${seenNear[1]}`;
    if (locale === "zh") return `发现于 ${seenNear[1]} 附近`;
    return `${seenNear[1]}付近で目撃`;
  }

  const protectedProfile = source.match(/^Scan to open\s+(.+)'s protected profile$/i);
  if (protectedProfile) {
    if (locale === "th") return `สแกนเพื่อเปิดโปรไฟล์ที่ได้รับการปกป้องของ ${protectedProfile[1]}`;
    if (locale === "zh") return `扫码打开 ${protectedProfile[1]} 的受保护资料`;
    return `スキャンして${protectedProfile[1]}の保護プロフィールを開く`;
  }

  const scoredMatch = source.match(/^(.+)\s+match score$/i);
  if (scoredMatch) {
    if (locale === "th") return `คะแนนการจับคู่ ${scoredMatch[1]}`;
    if (locale === "zh") return `匹配分数 ${scoredMatch[1]}`;
    return `一致スコア ${scoredMatch[1]}`;
  }

  const hoursAgo = source.match(/^(\d+)h ago$/i);
  if (hoursAgo) {
    if (locale === "th") return `${hoursAgo[1]} ชม. ที่แล้ว`;
    if (locale === "zh") return `${hoursAgo[1]} 小时前`;
    return `${hoursAgo[1]}時間前`;
  }

  const daysAgo = source.match(/^(\d+) days? ago$/i);
  if (daysAgo) {
    if (locale === "th") return `${daysAgo[1]} วันที่แล้ว`;
    if (locale === "zh") return `${daysAgo[1]} 天前`;
    return `${daysAgo[1]}日前`;
  }

  const kmAway = source.match(/^([\d.]+) km away$/i);
  if (kmAway) {
    if (locale === "th") return `ห่าง ${kmAway[1]} กม.`;
    if (locale === "zh") return `距 ${kmAway[1]} 公里`;
    return `${kmAway[1]}km先`;
  }

  const vetPlaces = source.match(/^(\d+) veterinary places within (\d+) km$/i);
  if (vetPlaces) {
    if (locale === "th") return `${vetPlaces[1]} สถานพยาบาลสัตว์ภายใน ${vetPlaces[2]} กม.`;
    if (locale === "zh") return `${vetPlaces[2]} 公里内 ${vetPlaces[1]} 处兽医机构`;
    return `半径${vetPlaces[2]}km以内の${vetPlaces[1]}か所の獣医施設`;
  }

  const casesNearby = source.match(/^(\d+) cases? nearby$/i);
  if (casesNearby) {
    if (locale === "th") return `${casesNearby[1]} เคสใกล้ตัว`;
    if (locale === "zh") return `附近 ${casesNearby[1]} 个案`;
    return `近くの${casesNearby[1]}件のケース`;
  }

  const listingsShown = source.match(/^(\d+) listings? shown$/i);
  if (listingsShown) {
    if (locale === "th") return `${listingsShown[1]} รายการที่แสดง`;
    if (locale === "zh") return `显示 ${listingsShown[1]} 条`;
    return `${listingsShown[1]}件表示`;
  }

  const pctMonth = source.match(/^(\d+)% this month$/i);
  if (pctMonth) {
    if (locale === "th") return `${pctMonth[1]}% เดือนนี้`;
    if (locale === "zh") return `本月 ${pctMonth[1]}%`;
    return `今月${pctMonth[1]}%`;
  }

  const nearestCareLabel = source.match(/^Nearest care:\s+(.+)\s+·\s+([\d.]+) km$/i);
  if (nearestCareLabel) {
    if (locale === "th") return `สถานพยาบาลสัตว์ใกล้สุด: ${nearestCareLabel[1]} · ${nearestCareLabel[2]} กม.`;
    if (locale === "zh") return `最近医疗点：${nearestCareLabel[1]} · ${nearestCareLabel[2]} 公里`;
    return `最寄りの動物医療：${nearestCareLabel[1]} · ${nearestCareLabel[2]}km`;
  }

  return source;
}
